package MetodosMath;


public class random {
    public static void main(String[] args) {

        double num= Math.random();

        System.out.println("Número Aleatório 1: "+num);
        System.out.println("Número Aleatório 2: "+num*5);
        System.out.println("Número Aleatório 3: "+Math.round(num*5));

    }
}